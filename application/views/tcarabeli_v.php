<p>Cara Pembelian github</p>
<p>1. Pilih Barang</p>
<p>&nbsp;</p>
<p>Anda dapat memilih barang yang ada pada website kami dengan mengklik tombol Beli sekarang pada masing-masing detail barang.</p>
<p>2. Keranjang Belanja</p>
<p>&nbsp;</p>
<p>Barang yang anda pilih akan muncul pada halaman Keranjang Belanja. Anda dapat memilih lagi barang yang ada diwebsite kami dengan mengklik link Pilih Barang. Jika anda sudah selesai dalam memilih barang, selanjutnya anda dapat memilih link Selesai Memilih Barang untuk melanjutkan ke form input identitas</p>
<p>3. Masukkan Identitas Anda</p>
<p>&nbsp;</p>
<p>Setelah melakukan pemilihan barang, selanjutnya anda dapat memasukkan identitas dan melengkapi data-data anda untuk mempermudah kami dalam pengiriman barang yang anda pesan.</p>
<p>4. Pembayaran</p>
<p>&nbsp;</p>
<p>Sebelum kami mengirimkan barang yang anda pesan, terlebih dahulu anda harus membayar sebesar pembelian barang anda ditambah biaya transport melalui <br />
  Setelah anda mentransfer pembayaran, harap anda menghubungi kami agar kami segera mengecek transaksi dari anda dan untuk selanjutnya memprosesnya.</p>
<p>5. Pengiriman Barang</p>
<p>&nbsp;</p>
<p>Kami akan segera mengirimkan barang pesanan anda ke alamat yang anda masukkan pada form identitas diri. </p>
